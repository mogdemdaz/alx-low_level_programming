/**
 * _strncat - check the code
 * @dest: params 1
 * @src: params 2
 * @n: params 3
 * Return: Always 0.
 */
char *_strncat(char *dest, char *src, int n)
{
int i, count = 0;
for (i = 0; dest[i] != '\0'; i++)
count++;


for (i = 0; src[i] && i < n; i++)
{
dest[count++] = src[i];
}

return (dest);
}
